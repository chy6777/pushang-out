/**
 * 语言管理 composable（全局单例状态）
 */
window._i18nState = window._i18nState || {
  lang: Vue.ref(localStorage.getItem('pushang_lang') || 'zh')
};

window.useI18n = function () {
  const lang = window._i18nState.lang;

  const setLang = (newLang) => {
    lang.value = newLang;
    localStorage.setItem('pushang_lang', newLang);
    document.documentElement.lang = newLang === 'zh' ? 'zh-CN' : 'en';
  };

  const toggleLang = () => {
    setLang(lang.value === 'zh' ? 'en' : 'zh');
  };

  const t = (key) => {
    const messages = window.I18N[lang.value] || window.I18N.zh;
    return messages[key] || key;
  };

  // 获取当前语言的数据（公司信息、产品等）
  const getData = () => {
    if (lang.value === 'en') {
      return window.I18N_DATA_EN;
    }
    return window.STATIC_DATA;
  };

  const getCompanyInfo = () => {
    const data = getData();
    return data.companyInfo;
  };

  const getCategories = () => {
    const data = getData();
    return data.categories;
  };

  const getProducts = (categoryId) => {
    let products = getData().products;
    if (categoryId) {
      products = products.filter(p => p.categoryId === categoryId);
    }
    return products;
  };

  const getFeaturedProducts = () => {
    return getData().products.filter(p => p.isFeatured === 1);
  };

  const getProductDetail = (id) => {
    return getData().products.find(p => p.id === parseInt(id));
  };

  const getStages = () => {
    return getData().stages;
  };

  const getProcesses = (stageId) => {
    let processes = getData().processes;
    if (stageId) {
      processes = processes.filter(p => p.stageId === parseInt(stageId));
    }
    return processes;
  };

  const getNewsList = () => {
    return getData().news;
  };

  const getNewsDetail = (id) => {
    return getData().news.find(n => n.id === parseInt(id));
  };

  const getGalleryList = (category) => {
    let gallery = getData().gallery;
    if (category) {
      gallery = gallery.filter(g => g.category === category);
    }
    return gallery;
  };

  const getGalleryCategories = () => {
    return getData().galleryCategories;
  };

  return {
    lang,
    setLang,
    toggleLang,
    t,
    getData,
    getCompanyInfo,
    getCategories,
    getProducts,
    getFeaturedProducts,
    getProductDetail,
    getStages,
    getProcesses,
    getNewsList,
    getNewsDetail,
    getGalleryList,
    getGalleryCategories
  };
};
